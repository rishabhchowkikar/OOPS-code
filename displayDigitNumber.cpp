#include <iostream>
using namespace std;
int main()
{
    int numbers;
    cout << "Enter your number: ";
    cin >> numbers;
    if (numbers == 1)
    {
        cout << "ONE";
    }
    else if (numbers == 2)
    {
        cout << "TWO";
    }
    else if (numbers == 3)
    {
        cout << "THREE";
    }
    else if (numbers == 4)
    {
        cout << "FOUR";
    }
    else if (numbers == 5)
    {
        cout << "FIVE";
    }
    else if (numbers == 6)
    {
        cout << "SIX";
    }
    else if (numbers == 7)
    {
        cout << "SEVEN";
    }
    else if (numbers == 8)
    {
        cout << "EIGHT";
    }
    else if (numbers == 9)
    {
        cout << "NINE";
    }
    else if (numbers == 10)
    {
        cout << "TEN";
    }
    else
    {
        cout << "Invalid number";
    }
    return 0;
}